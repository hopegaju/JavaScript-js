// Operators

let c=10;
c+=5;
console.log(c); //15

// Arithmetic Operators

let d=10;
console.log(d+5); //15
console.log(d-5); //5
console.log(d*5); //50
console.log(d/5); //2

// Assignment Operators

let e=10;
e+=5;
console.log(e); //15

// Logical Operators
let h=true;
let i=false;
console.log(h&&i); //false
console.log(h||i); //true

// Comparison Operators
let f=10;
let g=20;
console.log(f<g); //true
console.log(f>g); //false
console.log(f==g); //false

// ==: compare if the value is the same, ===:compare if the value is the same and also the type is the same
// Type Coercion
console.log("10"+5); //105
console.log(10+"5"); //105
console.log(10+5+""); //15
